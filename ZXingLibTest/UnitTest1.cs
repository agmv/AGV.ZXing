    private byte[] loadResource(string resourceName) {
        Stream? s = Assembly.GetExecutingAssembly().GetManifestResourceStream(resourceName);
        if (s == null)
            throw new Exception();
        var ms = new MemoryStream();
        s.CopyTo(ms);
        return ms.ToArray();        
    }

